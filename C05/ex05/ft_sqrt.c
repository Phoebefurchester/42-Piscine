int	ft_sqrt(int nb)
{
	long int	i;

	i = 1;
	if (nb == 0 || nb < 0)
		return (0);
	while (i * i < nb)
	{
		i++;
	}
	if (nb == i * i)
		return (i);
	else
		return (0);
}
